
public class Hanghoa {
	private String mhh;
	private String tenhh;
	private int donggia;
	private String xuatxu;
	private String loaisp;
	public Hanghoa() {
		
	}
	public Hanghoa(String mhh, String tenhh, int donggia, String xuatxu, String loaisp) {
		super();
		this.mhh = mhh;
		this.tenhh = tenhh;
		this.donggia = donggia;
		this.xuatxu = xuatxu;
		this.loaisp = loaisp;
	}
	public String getMhh() {
		return mhh;
	}
	public void setMhh(String mhh) {
		this.mhh = mhh;
	}
	public String getTenhh() {
		return tenhh;
	}
	public void setTenhh(String tenhh) {
		this.tenhh = tenhh;
	}
	public int getDonggia() {
		return donggia;
	}
	public void setDonggia(int donggia) {
		this.donggia = donggia;
	}
	public String getXuatxu() {
		return xuatxu;
	}
	public void setXuatxu(String xuatxu) {
		this.xuatxu = xuatxu;
	}
	public String getLoaisp() {
		return loaisp;
	}
	public void setLoaisp(String loaisp) {
		this.loaisp = loaisp;
	}
	
	
}
